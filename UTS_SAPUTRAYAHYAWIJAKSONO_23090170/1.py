bilangan_pertama = float(input("Masukkan bilangan pertama: "))

bilangan_kedua = float(input("Masukkan bilangan kedua: "))

jumlah = bilangan_pertama + bilangan_kedua
print("Hasil penjumlahan:", jumlah)

kurang = bilangan_pertama - bilangan_kedua
print("Hasil pengurangan:", kurang)

modulus = bilangan_pertama % bilangan_kedua
print("Hasil modulus:", modulus)